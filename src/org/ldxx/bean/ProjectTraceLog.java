package org.ldxx.bean;

public class ProjectTraceLog {
    private String ptlId;

    private String prjName;

    private String manageNo;

    private String ptlName;

    private String ptlTime;

    private String ptlLog;

    
    
	public String getPtlId() {
		return ptlId;
	}

	public void setPtlId(String ptlId) {
		this.ptlId = ptlId;
	}

	public String getPrjName() {
		return prjName;
	}

	public void setPrjName(String prjName) {
		this.prjName = prjName;
	}

	public String getManageNo() {
		return manageNo;
	}

	public void setManageNo(String manageNo) {
		this.manageNo = manageNo;
	}

	public String getPtlName() {
		return ptlName;
	}

	public void setPtlName(String ptlName) {
		this.ptlName = ptlName;
	}

	public String getPtlTime() {
		return ptlTime;
	}

	public void setPtlTime(String ptlTime) {
		this.ptlTime = ptlTime;
	}

	public String getPtlLog() {
		return ptlLog;
	}

	public void setPtlLog(String ptlLog) {
		this.ptlLog = ptlLog;
	}
    
}