package org.ldxx.bean;

public class Job {

	private String jobId;
	private String jobFirst;
	private String jobSecond;
	private String jobLvId;
	private String jobType;
	
	private String jobLvName;
	private int jobLvSize;
	
	
	
	public String getJobLvName() {
		return jobLvName;
	}
	public void setJobLvName(String jobLvName) {
		this.jobLvName = jobLvName;
	}
	public int getJobLvSize() {
		return jobLvSize;
	}
	public void setJobLvSize(int jobLvSize) {
		this.jobLvSize = jobLvSize;
	}
	public String getJobId() {
		return jobId;
	}
	public void setJobId(String jobId) {
		this.jobId = jobId;
	}
	public String getJobFirst() {
		return jobFirst;
	}
	public void setJobFirst(String jobFirst) {
		this.jobFirst = jobFirst;
	}
	public String getJobSecond() {
		return jobSecond;
	}
	public void setJobSecond(String jobSecond) {
		this.jobSecond = jobSecond;
	}
	public String getJobLvId() {
		return jobLvId;
	}
	public void setJobLvId(String jobLvId) {
		this.jobLvId = jobLvId;
	}
	public String getJobType() {
		return jobType;
	}
	public void setJobType(String jobType) {
		this.jobType = jobType;
	}

	
}
